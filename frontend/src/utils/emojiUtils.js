/**
 * Helper function to add an emoji to text
 * @param {string} text - The original text
 * @param {string} emoji - The emoji to add
 * @returns {string} - The text with the emoji added
 */
export const addEmojiToText = (text, emoji) => {
  return text + emoji;
};

/**
 * Get a random emoji from a collection
 * @param {Array} collection - Array of emojis
 * @returns {string} - A random emoji
 */
export const getRandomEmoji = (collection = POPULAR_EMOJIS) => {
  const randomIndex = Math.floor(Math.random() * collection.length);
  return collection[randomIndex];
};

